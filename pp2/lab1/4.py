"""CREATING VARIABLES"""
x=5
y="hello,world"
print(x)
print(y)

#2nd example
x = 4       # x is of type int
x = "Sally" # x is now of type str
print(x)

"""CASTING"""
x = str(3)    # x will be '3'
y = int(3)    # y will be 3
z = float(3)  # z will be 3.0
print(x)
print(y)
print(z)

"""GET THE TYPE"""

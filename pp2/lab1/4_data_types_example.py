x = 5
print(type(x))

x = "Hello"
print(type(x))

x = 5.5
print(type(x))

x = 1j	
print(type(x))

x = ["apple", "banana", "cherry"]
print(type(x))

x = ("apple", "banana", "cherry")
print(type(x))

x = range(9)
print(type(x))

x = dict(name="John", age=36)
print(type(x))

x = set(("apple", "banana", "cherry"))
print(type(x))

x = frozenset(("apple", "banana", "cherry"))	
print(type(x))

x = bool(5)	
print(type(x))

x = bytes(5)	
print(type(x))

x = bytearray(5)	
print(type(x))

x = memoryview(bytes(5))	
print(type(x))
